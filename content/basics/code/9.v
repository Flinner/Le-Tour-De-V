/*
* Basic Array Concepts
*/

mut nums := [1, 2, 3]
println(nums) // `[1, 2, 3]`

// accessing arrays
println(nums[0]) // `1`
println(nums[1]) // `2`

// setting elements of array
// not that `nums` should be `mut`!
nums[1] = 5
println(nums) // `[1, 5, 3]`

